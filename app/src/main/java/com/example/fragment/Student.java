package com.example.fragment;

public class Student {
    String name;
    String id;
    String grade;
    double point;
    Integer thumbnail;
    public Student() {

    }

    public Student(String name, String id, String grade, double point, Integer thumbnail) {
        this.name = name;
        this.id = id;
        this.grade = grade;
        this.point = point;
        this.thumbnail = thumbnail;
    }
}
